	public void initSimulator(String gensize, String gensampled, String popsize,
			String pfsize, String mut, String rec, String seed) {
		generations = Integer.parseInt(gensize);
		jLabel8.setText(gensampled + "/" + gensize);
		jLabel9.setText(popsize);
		jLabel10.setText(pfsize);
		jLabel11.setText(rec + "/" + mut);
		jLabel12.setText(seed);
		simulator = new Simulator(generations, Integer.parseInt(gensampled),
				Integer.parseInt(popsize), Integer.parseInt(pfsize),
				Integer.parseInt(mut), Integer.parseInt(rec),
				Integer.parseInt(seed));
		jToggleButton1.setEnabled(true);
		jToggleButton1.setSelected(false);
		jProgressBar1.setString("0%");
//		System.out.println("jprogress = 0%");
	}

	public boolean isSimulationFinished() {
		return bSim;
	}
	
	public StringReader getDataset() {
		if (!bSim) {
			return null;
		}
		return new StringReader(simulator.writeLastPopGenerations());
	}

