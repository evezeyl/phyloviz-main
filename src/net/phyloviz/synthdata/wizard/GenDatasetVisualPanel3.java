	private Task task;
	private boolean bSim;
	private Simulator simulator;
	private int generations;

	class Task extends SwingWorker<Void, Void> {

		@Override
		public Void doInBackground() {

			for (int i = 0; i < generations; i++) {
				if (isCancelled()) {
					bSim = false;
					jProgressBar1.setString("Simulation canceled!");
					return null;
				}
				simulator.runOneStep();
				int percent = i * 100 / generations;
				setProgress(percent);
				jProgressBar1.setString("Simulating... generation " +
						i + " of " + generations + " (" + percent + " %)");
			}
//			System.out.println("Cycle generations end.");
			jProgressBar1.setValue(100);
			jToggleButton1.setEnabled(false);
			jProgressBar1.setString("Simulation completed!");
			bSim = true;
			return null;
			//TODO: System.out.println(simulator.dumpLastPopSTs());
		}

		@Override
		public void done() {
			setCursor(null); //turn off the wait cursor
		}
	}
